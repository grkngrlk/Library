     public int CheckPerson (String C){
        
        try {
          st=conn.createStatement();
          String query="select Tcno from person";
          ResultSet rs=st.executeQuery(query);
          while(rs.next())
          {
              if(rs.getString(1).equals(C))
              {
                   return 1;
              }
          }   
        } catch (Exception e) {
            e.printStackTrace();
        }
return 0;
    }
