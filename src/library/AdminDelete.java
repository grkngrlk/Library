    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (jTextField1.getText().equals("") && (jRadioButton1.isSelected()==false) ){

            JOptionPane.showMessageDialog(null, "Please Fill The Boxes" ,"Warning",JOptionPane.WARNING_MESSAGE);
        }   
        
        else if( (jRadioButton1.isSelected()==false) && CheckPerson(jTextField1.getText())==0){
           JOptionPane.showMessageDialog(null, "This Id is Not Exist","Warning",JOptionPane.WARNING_MESSAGE);

        }
        else{
            
            if((jRadioButton1.isSelected()==true)){
                
                try {
                    st=conn.createStatement();
                    String query="DELETE FROM operations";
                    int res=JOptionPane.showConfirmDialog(this, "Are you sure?", "", JOptionPane.OK_CANCEL_OPTION);
                    if(res==JOptionPane.OK_OPTION)
                    {
                        st.executeUpdate(query);
                        JOptionPane.showMessageDialog(null, "All Records Deleted.","",JOptionPane.PLAIN_MESSAGE);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            else{
                try {
                    st=conn.createStatement();
                    String query="DELETE FROM operations where OpID="+jTextField1.getText();
                    int res=JOptionPane.showConfirmDialog(this, "Are you sure?", "", JOptionPane.OK_CANCEL_OPTION);
                    if(res==JOptionPane.OK_OPTION)
                    {
                        st.executeUpdate(query);
                        JOptionPane.showMessageDialog(null, "Record Deleted.","",JOptionPane.PLAIN_MESSAGE);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
               
            
        }



// TODO add your handling code here:
    }//GEN-LAST:event_jButton1MouseClicked

    public int CheckPerson (String C){
        
        try {
          st=conn.createStatement();
          String query="select OpID from operations";
          ResultSet rs=st.executeQuery(query);
          while(rs.next())
          {
              if(rs.getString(1).equals(C))
              {
                   return 1;
              }
          }   
        } catch (Exception e) {
            e.printStackTrace();
        }
return 0;
    }
    
